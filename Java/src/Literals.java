
public class Literals {

    public static void main(String[] args) {
        // Literals -> A constant value which can be assigned to the variable is called a literal.

        byte age = 34;
        char ch = 'A';
        float f1 = 2.3f;
        double d1 = 4.6D;
        long l1 = 123456789L;
        short s1 = 1;

        // String is a method to print a string
        String str = "Hello Ankit1";

        System.out.println("Age: " + age + ", Character: " + ch + ", Float: " + f1 + ", Double: " + d1 + ", Long: " + l1
                + ", Short: " + s1);
        System.out.println(str);
    }
}
